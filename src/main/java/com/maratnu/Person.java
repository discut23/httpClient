package com.maratnu;

public class Person {
    private String name;
    private String homeworld;
    private String uid;
    private String url;

    public String getHomeworld() {
        return homeworld;
    }

    public String getName() {
        return name;
    }
    public String getUid() {
        return uid;
    }
    public String getUrl() {
        return url;
    }
}
